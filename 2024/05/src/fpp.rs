pub struct IncidenceMatrix {
    pub n:usize,                    // N 
    pub dim: usize,                 // N*N + N + 1
    pub matrix:Vec<Vec<bool>>,      // Incidence matrix
    pub row_hashes:HashSet<u128>    // Lookup of row substrings for fast 
                                    // evaluation of column permutations
}

impl Clone for IncidenceMatrix {
    fn clone(&self) -> IncidenceMatrix {
        let mut matrix:Vec<Vec<bool>> = Vec::new();
        for i in 0..self.dim {
            matrix.push(Vec::<bool>::new());
            for j in 0..self.dim {
                matrix[i].push(self.matrix[i][j]);
            }
        }
        return IncidenceMatrix{n:self.n, dim:self.dim, matrix:matrix, row_hashes:self.row_hashes.clone()};
    }
}

impl IncidenceMatrix {
    
    /// Construct a new IncidenceMatrix based on the provided string.
    pub fn new(input_str:&str) -> IncidenceMatrix {
        // Lookup of dimensions of fpp incidence matrices for orders 2,3,4,5,7,8
        let matrix_dimensions:Vec<usize> = vec![0,0,7,13,21,31,0,57,73];
        let input = input_str.trim().to_string();
        let lines:Vec<&str> = input.split("\n").collect();
        let dim = lines.len();
        if dim == 0 {
            panic!("Empty incidence matrix provided");
        }
        let mut n:usize = 0;
        let mut n_found = false;
        for i in 0..matrix_dimensions.len() {
            if matrix_dimensions[i] == dim {
                n = i;
                n_found = true;
            }
        }
        if !n_found {
            panic!("Only incidence matrices of orders 2,3,4,5,7,8 are supported");
        }
        let incidences_per = n + 1;
        let mut matrix:Vec<Vec<bool>> = Vec::new();
        for i in 0..dim {
            matrix.push(Vec::<bool>::new());
            for _ in 0..dim {
                matrix[i].push(false);
            }
        }
        let lines:Vec<&str> = input.split("\n").collect();
        if lines.len() != dim {
            panic!("Input matrix has incorrect number of rows for order {}", n);
        }
        let mut row_count = 0;
        for line in lines {
            let cols:Vec<&str> = line.split(" ").collect();
            if cols.len() != dim {
                panic!("Input matrix has incorrect number of columns for order {}", n);
            }
            let mut col_count = 0;
            for col in cols {
                if col == "1" {
                    matrix[row_count][col_count] = true;
                }
                col_count += 1;
            }
            row_count += 1;
        }
        // Validate matrix
        for i in 0..dim {
            let mut row_incidence_count = 0;
            let mut col_incidence_count = 0;
            for j in 0..dim {
                if matrix[i][j] {
                    row_incidence_count += 1;
                }
                if matrix[j][i] {
                    col_incidence_count += 1;
                }
            }
            if row_incidence_count != incidences_per {
                panic!("Input matrix has incorrect number of incidences in row {}", i);
            }
            if col_incidence_count != incidences_per {
                panic!("Input matrix has incorrect number of incidences in column {}", i);
            }
        }
        for i in 0..dim {
            for j in 0..dim {
                if j == i {
                    continue;
                }
                let mut shared_row_count = 0;
                let mut shared_col_count = 0;
                for k in 0..dim {
                    if matrix[i][k] && matrix[j][k] {
                        shared_row_count += 1;
                    }
                    if matrix[k][i] && matrix[k][j] {
                        shared_col_count += 1;
                    }
                }
                if shared_row_count != 1 {
                    panic!("Input matrix has incorrect number of shared incidences between rows {} and {}", i, j)
                }
                if shared_col_count != 1 {
                    panic!("Input matrix has incorrect number of shared incidences between columns {} and {}", i, j)
                }
            }
        }
        // Build partial row hashset for quickly detecting non-automorphic 
        // permutations
        let mut row_hashes:HashSet<u128> = HashSet::new();
        for row in 0..dim {
            for col in 0..dim {
                let mut row_hash:u128 = 0;
                for i in 0..=col {
                    if matrix[row][i] {
                        row_hash |= 1 << i;
                    }
                }
                row_hashes.insert(row_hash);
            }
        }
        return IncidenceMatrix{n:n, dim:dim, matrix:matrix, row_hashes:row_hashes};
    }

    /// Returns false if the provided partial column permutation produces any 
    /// rows that are not present in the initial incidence matrix
    pub fn permutation_potentially_automorphic(&self, permutation:&Vec<usize>) -> bool {
        for row in 0..self.dim {
            let mut row_hash:u128 = 0;
            for col in 0..permutation.len() {
                if self.permuted_val(row, col, permutation) {
                    row_hash |= 1 << col;
                }
            }
            if !self.row_hashes.contains(&row_hash) {
                return false;
            }
        }
        return true;
    }

    /// Returns the value of the indidence matrix at the specified row and 
    /// column given the provided column permutations.
    pub fn permuted_val(&self, row:usize, col:usize, permutation:&Vec<usize>) -> bool {
        if col >= permutation.len() {
            panic!("Column exceeds permutation length")
        }
        if col >= self.dim {
            panic!("Column exceeds matrix dimensions");
        }
        if row >= self.dim {
            panic!("Row exceeds matrix dimensions");
        }
        if permutation[col] >= self.dim {
            panic!("Permutation for col {} maps to a column that exceeds matrix dimensions", col)
        }
        return self.matrix[row][permutation[col]];
    }
    
    /// Print this incidence matrix to stdout
    pub fn print(&self) {
        for i in 0..self.dim {
            for j in 0..self.dim {
                print!("{} ", if self.matrix[i][j] {1} else {0});
            }
            print!("\n");
        }
    }
}
